CREATE TABLE CHINHANH (IDCN CHAR(10) PRIMARY KEY NOT NULL, TENCNHANH NVARCHAR(255) NOT NULL, HOTLINE VARCHAR(20), DIACHI NVARCHAR(255));
CREATE TABLE CHUCVU   (IDCV CHAR(10) PRIMARY KEY NOT NULL, TENCV NVARCHAR(255) NOT NULL);
CREATE TABLE PHONGBAN (IDPB CHAR(10) PRIMARY KEY NOT NULL, TENPB NVARCHAR(255) NOT NULL, DIACHI NVARCHAR(255), NGAYTHANHLAP DATE);
CREATE TABLE TRINHDO  (IDTD CHAR(10) PRIMARY KEY NOT NULL, TENTD NVARCHAR(255) NOT NULL, CHUYENNGANH NVARCHAR(255));

CREATE TABLE NHANVIEN (
    IDNV CHAR(10) PRIMARY KEY NOT NULL, TENNV NVARCHAR(255) NOT NULL, GIOITINH NVARCHAR(10),
    NGAYSINH DATE, CCCD CHAR(12), EMAIL VARCHAR(255), DIENTHOAI VARCHAR(15),
    DIACHI NVARCHAR(255), DANTOC NVARCHAR(50), TONGIAO NVARCHAR(50), HONNHAN NVARCHAR(50),
    TRINHDO CHAR(10) NOT NULL, CHUCVU CHAR(10) NOT NULL, PHONGBAN CHAR(10) NOT NULL, CHINHANH CHAR(10) NOT NULL,
    FOREIGN KEY (CHINHANH) REFERENCES CHINHANH(IDCN), FOREIGN KEY (CHUCVU)  REFERENCES CHUCVU(IDCV),
    FOREIGN KEY (TRINHDO)  REFERENCES TRINHDO(IDTD),  FOREIGN KEY (PHONGBAN) REFERENCES PHONGBAN(IDPB)
);

CREATE TABLE LOAIHD (IDLOAI CHAR(10) PRIMARY KEY NOT NULL, TENLOAI NVARCHAR(255) NOT NULL, THOIHAN INT, BHYT NVARCHAR(11), BHXH NVARCHAR(11), BHTN NVARCHAR(11));

CREATE TABLE HOPDONG (
    SODH CHAR(10) PRIMARY KEY NOT NULL, NGAYKY DATE, NGAYBATDAU DATE, NGAYKETTHUC DATE,
    LUONGCOBAN DECIMAL(15,2), TRANGTHAI VARCHAR(20), IDNV CHAR(10) NOT NULL, LOAIHD CHAR(10) NOT NULL,
    FOREIGN KEY (IDNV) REFERENCES NHANVIEN(IDNV), FOREIGN KEY (LOAIHD) REFERENCES LOAIHD(IDLOAI)
);

CREATE TABLE BANGCHAMCONG (
    IDBC CHAR(10) PRIMARY KEY NOT NULL, IDNV CHAR(10) NOT NULL, THANG TINYINT, NAM SMALLINT,
    SOGIOTANGCA DECIMAL(5,2), SONGAYNGHI INT, SONGAYDITRE INT, TONGNGAYLAM INT, TRANGTHAI NVARCHAR(50),
    FOREIGN KEY (IDNV) REFERENCES NHANVIEN(IDNV)
);

CREATE TABLE LOAICONG (IDLC CHAR(10) PRIMARY KEY NOT NULL, TENLC NVARCHAR(50) NOT NULL, MOTA NVARCHAR(255));

CREATE TABLE CHITIET_BANGCONG (
    IDCT CHAR(10) PRIMARY KEY NOT NULL, IDBC CHAR(10) NOT NULL, NGAYLAM INT, LOAICONG CHAR(10) NOT NULL,
    FOREIGN KEY (IDBC) REFERENCES BANGCHAMCONG(IDBC), FOREIGN KEY (LOAICONG) REFERENCES LOAICONG(IDLC)
);

CREATE TABLE BANGLUONG (
    IDBL CHAR(10) PRIMARY KEY NOT NULL, IDBC CHAR(10) NOT NULL, LUONGCOBAN DECIMAL(15,2),
    LUONGTHUCTE DECIMAL(15,2), THUETNCN DECIMAL(10,2), LUONGTHUONG DECIMAL(15,2),
    PHUCAPCHUCVU DECIMAL(15,2), KHOANTRUBAOHIEM DECIMAL(15,2), PHUCAPKHAC DECIMAL(15,2),
    KHOANTRUKHAC DECIMAL(15,2), THUCNHAN DECIMAL(15,2),
    FOREIGN KEY (IDBC) REFERENCES BANGCHAMCONG(IDBC)
);

CREATE TABLE TUYENDUNG (
    MATD CHAR(10) PRIMARY KEY, IDCN CHAR(10), VITRITD NVARCHAR(255), DOTUOI INT, GIOITINH NVARCHAR(10),
    SOLUONG INT, HANTD DATE, LUONGTOITHIEU DECIMAL(10,2), LUONGTOIDA DECIMAL(10,2),
    SOHOSODANAOP INT, SOHOSODATUYEN INT, TRANGTHAI NVARCHAR(50),
    FOREIGN KEY (IDCN) REFERENCES CHINHANH(IDCN)
);
GO

